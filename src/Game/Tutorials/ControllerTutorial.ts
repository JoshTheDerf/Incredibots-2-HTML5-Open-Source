	public Update():void {
		super.Update();
		this.sSky.Update(this.hasZoomed, this.hasPanned);

		if (this.hasZoomed) {
			this.sGround1.width = this.World2ScreenX(29.34) - this.World2ScreenX(0);
			this.sGround1.scale.y = this.sGround1.scale.x;
			this.sGround2.scale.x = this.sGround1.scale.x;
			this.sGround2.scale.y = this.sGround2.scale.x;
			this.sGround3.scale.x = this.sGround1.scale.x;
			this.sGround3.scale.y = this.sGround3.scale.x;
		}
		if (this.hasZoomed || this.hasPanned) {
			this.sGround1.visible = (this.World2ScreenX(-41) > 0);
			this.sGround2.visible = (this.World2ScreenX(24) > 0 && this.World2ScreenX(-39) < 800);
			this.sGround3.visible = (this.World2ScreenX(19) < 800);
			if (this.sGround1.visible) {
				this.sGround1.x = this.World2ScreenX(-77);
				this.sGround1.y = this.World2ScreenY(-15.3);
			}
			if (this.sGround2.visible) {
				this.sGround2.x = this.World2ScreenX(-75);
				this.sGround2.y = this.World2ScreenY(-15.3);
			}
			if (this.sGround3.visible) {
				this.sGround3.x = this.World2ScreenX(-75);
				this.sGround3.y = this.World2ScreenY(-15.3);
			}
		}
		this.hasPanned = false;
		this.hasZoomed = false;
	}

